#!/bin/bash

# ============================================================
#  xray-manager
#  Commands: setup | create | print [username] | export
# ============================================================

set -e

# ============================================================
#  Default Variables — edit these or override at runtime
#  These are also saved/loaded from SAVED_VARS_FILE after setup
# ============================================================
CONFIG="/usr/local/etc/xray/config.json"
NGINX_SITES="/etc/nginx/sites-available"
NGINX_ENABLED="/etc/nginx/sites-enabled"
SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SAVED_VARS_FILE="${SCRIPT_DIR}/xray-manager.conf"
EXPORT_FILE="$HOME/xray-configs.txt"

DEFAULT_SERVER_IP=""
DEFAULT_CDN_DOMAIN="bazi.bazistation.online"
DEFAULT_VMESS_HOST="varzesh3.com"
DEFAULT_REALITY_SNI="varzesh3.com"
DEFAULT_CDN_PORT="443"
DEFAULT_VMESS_PORT="8080"
DEFAULT_REALITY_PORT="8443"
VLESS_WS_PATH="/slark"
VLESS_GRPC_SERVICE="slarkg"
VMESS_PATH="/18E114BB-6E68-48E1-9A18-82B2C187403F"

# Xray internal listen ports (nginx proxies to these)
XRAY_VLESS_WS_PORT="10000"
XRAY_VLESS_GRPC_PORT="10001"
XRAY_VMESS_PORT="10080"
# ============================================================

# ---------- Colors ----------
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
BOLD='\033[1m'
DIM='\033[2m'
NC='\033[0m'

# ---------- Load saved vars (written by setup) ----------
load_saved_vars() {
  if [ -f "$SAVED_VARS_FILE" ]; then
    # shellcheck source=/dev/null
    source "$SAVED_VARS_FILE"
  fi
}

# ---------- Save vars to file ----------
save_vars() {
  cat > "$SAVED_VARS_FILE" <<EOF
# Xray Manager — saved defaults (generated by setup on $(date))
DEFAULT_SERVER_IP="$DEFAULT_SERVER_IP"
DEFAULT_CDN_DOMAIN="$DEFAULT_CDN_DOMAIN"
DEFAULT_VMESS_HOST="$DEFAULT_VMESS_HOST"
DEFAULT_REALITY_SNI="$DEFAULT_REALITY_SNI"
DEFAULT_CDN_PORT="$DEFAULT_CDN_PORT"
DEFAULT_VMESS_PORT="$DEFAULT_VMESS_PORT"
DEFAULT_REALITY_PORT="$DEFAULT_REALITY_PORT"
VLESS_WS_PATH="$VLESS_WS_PATH"
VLESS_GRPC_SERVICE="$VLESS_GRPC_SERVICE"
VMESS_PATH="$VMESS_PATH"
XRAY_VLESS_WS_PORT="$XRAY_VLESS_WS_PORT"
XRAY_VLESS_GRPC_PORT="$XRAY_VLESS_GRPC_PORT"
XRAY_VMESS_PORT="$XRAY_VMESS_PORT"
EOF
  echo -e "${GREEN}Defaults saved to ${SAVED_VARS_FILE}${NC}"
}

# ---------- Usage ----------
usage() {
  echo -e "${BOLD}Usage:${NC}"
  echo -e "  $0 setup               — First-time server setup (nginx + xray skeleton + certbot)"
  echo -e "  $0 create              — Add a new user"
  echo -e "  $0 print               — Print configs for ALL users"
  echo -e "  $0 print <username>    — Print configs for a specific user"
  echo -e "  $0 export              — Export all configs to ${EXPORT_FILE}"
  echo ""
  exit 1
}

# ---------- Auto-install a package via apt ----------
ensure_apt_pkg() {
  local cmd="$1" pkg="${2:-$1}"
  if ! command -v "$cmd" &>/dev/null; then
    echo -e "${CYAN}Installing ${pkg}...${NC}"
    apt-get update -qq && apt-get install -y -qq "$pkg"       && echo -e "${GREEN}${pkg} installed.${NC}"       || { echo -e "${RED}Failed to install ${pkg}. Install manually and re-run.${NC}"; exit 1; }
  fi
}

# ---------- Check / auto-install deps ----------
check_deps() {
  # Always needed
  ensure_apt_pkg jq
  ensure_apt_pkg openssl

  if [ "${1:-}" == "create" ]; then
    if ! command -v uuidgen &>/dev/null; then
      ensure_apt_pkg uuidgen uuid-runtime
    fi
    if ! command -v xray &>/dev/null; then
      echo -e "${RED}Error: 'xray' is not installed.${NC}"
      echo -e "${YELLOW}Install it with: bash -c \"$(curl -L https://github.com/XTLS/Xray-install/raw/main/install-release.sh)\" @ install${NC}"
      exit 1
    fi
  fi

  if [ "${1:-}" == "setup" ]; then
    ensure_apt_pkg nginx
    # certbot via snap is preferred but fall back to apt
    if ! command -v certbot &>/dev/null; then
      echo -e "${CYAN}Installing certbot...${NC}"
      if command -v snap &>/dev/null; then
        snap install --classic certbot           && ln -sf /snap/bin/certbot /usr/bin/certbot           && echo -e "${GREEN}certbot installed via snap.${NC}"
      else
        ensure_apt_pkg certbot
        ensure_apt_pkg python3-certbot-nginx
      fi
    fi
  fi

  if [ "${1:-}" != "setup" ] && config_needs_skeleton; then
    echo -e "${RED}Error: Xray config not found or empty at $CONFIG${NC}"
    echo -e "${YELLOW}Run '$0 setup' first.${NC}"
    exit 1
  fi
}

# ---------- Get server IP ----------
get_server_ip() {
  if [ -n "$DEFAULT_SERVER_IP" ]; then
    SERVER_IP="$DEFAULT_SERVER_IP"
  else
    read -rp "$(echo -e ${YELLOW}"Enter your server public IP: "${NC})" SERVER_IP
    if [ -z "$SERVER_IP" ]; then
      echo -e "${RED}Server IP cannot be empty.${NC}"; exit 1
    fi
  fi
}

# ---------- Build links for a user ----------
build_links() {
  local user="$1" uuid="$2" ip="$3" cdn="$4" vmhost="$5" rsni="$6" pubkey="$7" sid="$8"

  LINK_WS="vless://${uuid}@${cdn}:${DEFAULT_CDN_PORT}?encryption=none&security=tls&sni=${cdn}&insecure=0&allowInsecure=0&type=ws&host=${cdn}&path=%2F${VLESS_WS_PATH##/}#${user}_WS_CDN"

  LINK_GRPC="vless://${uuid}@${cdn}:${DEFAULT_CDN_PORT}?encryption=none&security=tls&sni=${cdn}&insecure=0&type=grpc&serviceName=${VLESS_GRPC_SERVICE}#${user}_gRPC_CDN"

  local vmess_json
  vmess_json=$(printf '{"v":"2","ps":"%s_VMess","add":"%s","port":"%s","id":"%s","aid":"0","scy":"auto","net":"httpupgrade","type":"none","host":"%s","path":"%s","tls":"","sni":"","alpn":""}' \
    "$user" "$ip" "$DEFAULT_VMESS_PORT" "$uuid" "$vmhost" "$VMESS_PATH")
  LINK_VMESS="vmess://$(echo "$vmess_json" | base64 -w 0)"

  if [ -n "$pubkey" ] && [ -n "$sid" ]; then
    LINK_REALITY="vless://${uuid}@${ip}:${DEFAULT_REALITY_PORT}?encryption=none&security=reality&sni=${rsni}&fp=chrome&pbk=${pubkey}&sid=${sid}&type=tcp&flow=xtls-rprx-vision#${user}_Reality"
  else
    LINK_REALITY="(Reality key/shortId not found in config)"
  fi
}

