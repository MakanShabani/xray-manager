print_user_links() {
  local user="$1"
  echo -e "\n${BOLD}${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
  echo -e "${BOLD}  User: ${GREEN}${user}${NC}"
  echo -e "${BOLD}${CYAN}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}"
  echo -e "${BOLD}[1] VLESS + WebSocket + TLS + CDN${NC}"
  echo -e "${DIM}${LINK_WS}${NC}\n"
  echo -e "${BOLD}[2] VLESS + gRPC + TLS + CDN${NC}"
